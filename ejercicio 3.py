#Hagamos un ejercicioo en donde el usurario ingresa una variable numerica y por ----- 
#por medio de una secuneta logica me permita validar si un resultado es correcto.

numero_ingresado = int(input("Ingresa un numero:  "))

#Aplicacion de if y else  
if numero_ingresado > 100:
    print("El numero que ingreso es mayor que 100")
else:
    print("El numero que ingreso es menor que 100")
